f = input('Input the Filename: ')
if f.endswith('.py'):
    print('The extension of the file is: Python')
elif f.endswith('.txt'):
    print('The extension of the file is: Text')
elif f.endswith('.html'):
    print('The extension of the file is: Hypertext Markup Language')
elif f.endswith('.php'):
    print('The extension of the file is: PHP')
elif f.endswith('.doc'):
    print('The extension of the file is: Document')
elif f.endswith('.ppt'):
    print('The extension of the file is: Powerpoint Presentation')
elif f.endswith('.css'):
    print('The extension of the file is: Cascading Style Sheets')
else :
    print('Not Applicable Now!!!')